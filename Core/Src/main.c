void UART_line_callback(uint8_t *data, uint16_t length) {
	if (is_get(data, length)) {
		uint8_t *msg;
		uint16_t msg_size = serialize(&sLight, &msg);
		UART_send(msg, msg_size);
	}
	if (deserialize(&sLight, data, length)) {
		update = true; // TODO double buffering
	}
}

